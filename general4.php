                <p style="text-align: justify;"><font color="green-white" size="5.5"><span><u>Introduction to Konso Zone </u></span></font></p>
                <p style="text-align: justify;"> <font size="3" color="black" ><span>Konso Zone is one among the Seventeen Zones in Southern Ethiopia. 
                Karat, capital of Konso Zone, is located at about 595 and 360 kms South West of Addis Ababa and Hawassa 
                respectively. The Land is characterized by hilly, mountainous intersected by gullies and valleys,
                 most of which have been under cultivation over a period of hundreds of years. Konso population 
                 living in this geographical location at present is about 275,237 (51.8% females & 48.2 % males) 
                 with annual population growth rate of 2.6%.Konso is known for its hard-working people with strong 
                 indigenous skill and knowledge of Soil and Water Conservation and Management Practice. 
                 They have managed to survive in marginal environment using the indigenous skills and 
                 knowledge that enabled them to make optimal use of unfavorable terrain and climatic 
                 conditions in innovative manner. This creative, indigenous and noble work culture has qualified 
                 Konso people for UN Green prize in 1995 and recognition and registration of Konso Landscape 
                 by UNSCO in 2010.Despite this spectacular deed, Konso has remained in food insecurity and poverty for decades. 
                 This is due to natural calamities and manmade factors. Unreliable rainfall; recurrent drought; 
                 depletion of natural resources; small land holdings; etc can be mentioned as factors 
                 that compelled the people in to distressed livelihood. Poor development of basic 
                 infrastructures and services can also be mentioned as another factors contributing 
                 to the problem.Recently the government and other development organizations have
                  been implementing different programs such as Food security; Poverty reduction; 
                  Rural development and Infrastructure development programs that can contribute 
                  to the socio-economic development of the community. Konso Development Association (KDA)
                   is one of the CSO working in the Konso Zone to contribute towards the socio-economic 
                   improvement of the community.
            </span></font></p>
